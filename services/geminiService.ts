import { GoogleGenAI, Type } from "@google/genai";
import { AnalysisResult } from "../types";

const ai = new GoogleGenAI({ apiKey: process.env.API_KEY });

/**
 * Analyzes a 360 degree panoramic image using Gemini Vision.
 * 
 * @param base64Image The base64 encoded image string (without the data prefix).
 * @param mimeType The mime type of the image.
 * @returns Parsed AnalysisResult.
 */
export const analyze360Image = async (
  base64Image: string,
  mimeType: string
): Promise<AnalysisResult> => {
  try {
    const response = await ai.models.generateContent({
      model: "gemini-2.5-flash",
      contents: {
        parts: [
          {
            inlineData: {
              data: base64Image,
              mimeType: mimeType,
            },
          },
          {
            text: `Analyze this 360-degree equirectangular panoramic image. 
            Provide a detailed description of the scene, list distinct key objects visible, and describe the overall atmosphere or mood.`,
          },
        ],
      },
      config: {
        responseMimeType: "application/json",
        responseSchema: {
          type: Type.OBJECT,
          properties: {
            description: {
              type: Type.STRING,
              description: "A comprehensive paragraph describing the entire scene.",
            },
            objects: {
              type: Type.ARRAY,
              items: { type: Type.STRING },
              description: "A list of key objects or landmarks visible in the panorama.",
            },
            atmosphere: {
              type: Type.STRING,
              description: "The mood, lighting, and general feeling of the location.",
            },
          },
          required: ["description", "objects", "atmosphere"],
        },
      },
    });

    if (response.text) {
      return JSON.parse(response.text) as AnalysisResult;
    }
    throw new Error("No response text from Gemini.");
  } catch (error) {
    console.error("Gemini analysis failed:", error);
    throw error;
  }
};

/**
 * Generates an edited version of the image using Gemini Nano Banana (gemini-2.5-flash-image).
 * 
 * @param base64Image The base64 encoded snapshot with visual mask applied.
 * @param mimeType The mime type.
 * @param prompt User instruction.
 * @param maskColorName The name of the color used for masking (e.g., "red", "blue").
 * @returns Base64 string of the generated image.
 */
export const generateEditedImage = async (
  base64Image: string,
  mimeType: string,
  prompt: string,
  maskColorName: string = "red"
): Promise<string> => {
  try {
    // Simplified prompt to avoid potential 500 errors from complex system instructions on this model
    const finalPrompt = `This image contains a ${maskColorName} masked area. 
Replace the content in the ${maskColorName} area with: ${prompt}.
The result must be photorealistic and blend seamlessly with the surrounding 360-degree environment lighting and perspective.`;

    const response = await ai.models.generateContent({
      model: 'gemini-2.5-flash-image',
      contents: {
        parts: [
          {
            inlineData: {
              data: base64Image,
              mimeType: mimeType,
            },
          },
          {
            text: finalPrompt,
          },
        ],
      },
    });

    // Extract image from response parts
    if (response.candidates?.[0]?.content?.parts) {
      for (const part of response.candidates[0].content.parts) {
        if (part.inlineData && part.inlineData.data) {
          return part.inlineData.data;
        }
      }
    }
    
    throw new Error("No image generated by Gemini.");
  } catch (error) {
    console.error("Gemini image generation failed:", error);
    throw error;
  }
};

