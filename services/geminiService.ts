/**
 * Finds normalized coordinates [x, y] for specific objects in a 360 panorama.
 */
export const findObjectsInPanorama = async (
  base64Image: string,
  mimeType: string,
  objects: string[]
): Promise<{ object: string; x: number; y: number }[]> => {
  try {
    const response = await ai.models.generateContent({
      model: "gemini-2.5-flash",
      contents: {
        parts: [
          {
            inlineData: {
              data: base64Image,
              mimeType: mimeType,
            },
          },
          {
            text: `In this 360-degree equirectangular image, find the center coordinates for the following objects: ${objects.join(", ")}.
Return the coordinates as normalized values from 0.0 to 1.0, where (0,0) is top-left and (1,1) is bottom-right.`,
          },
        ],
      },
      config: {
        responseMimeType: "application/json",
        responseSchema: {
          type: Type.ARRAY,
          items: {
            type: Type.OBJECT,
            properties: {
              object: { type: Type.STRING },
              x: { type: Type.NUMBER, description: "Normalized X coordinate (0-1)" },
              y: { type: Type.NUMBER, description: "Normalized Y coordinate (0-1)" },
            },
            required: ["object", "x", "y"],
          },
        },
      },
    });

    if (response.text) {
      return JSON.parse(response.text);
    }
    return [];
  } catch (error) {
    console.error("Gemini object finding failed:", error);
    return [];
  }
};
